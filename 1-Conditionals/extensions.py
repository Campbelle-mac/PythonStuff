x = input("File name: ")

if "gif" in x:
    print("image/gif")
elif "png" in x:
    print("image/png")
elif "jpg" in x:
    print("image/jpeg")
elif "jpeg" in x:
    print("image/jpeg")
elif "pdf" in x:
    print("application/pdf")
elif "txt" in x:
    print("application.txt")
elif "zip" in x:
    print("application.zip")
